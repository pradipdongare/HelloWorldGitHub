package com.csi.string;

public class IncrementDecrement {
public static void main(String[] args) {
	int i=4;
	i++;
	System.out.println((i));//increement by 1
	System.out.println(i++);//
	System.out.println(++i);
	System.out.println(i++ + ++i);
	System.out.println(i);
	System.out.println(++i + i++);
}
}
